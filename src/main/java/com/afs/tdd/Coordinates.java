package com.afs.tdd;

public class Coordinates {
    private int posX;
    private int posY;

    public Coordinates(int posX, int posY) {
        this.posX = posX;
        this.posX = posY;
    }

    public int getXPosition() {
        return posX;
    }

    public int getYPosition(){
        return posY;
    }

}
